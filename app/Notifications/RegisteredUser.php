<?php

namespace App\Notifications;

use Illuminate\Bus\Queueable;
use Illuminate\Notifications\Notification;
use Illuminate\Contracts\Queue\ShouldQueue;
use Illuminate\Notifications\Messages\MailMessage;

class RegisteredUser extends Notification
{
    use Queueable;

    /**
     * Create a new notification instance.
     *
     * @return void
     */
    public function __construct()
    {
        //
    }

    /**
     * Get the notification's delivery channels.
     *
     * @param  mixed  $notifiable
     * @return array
     */
    public function via($notifiable)
    {
        return ['mail'];
    }

    /**
     * Get the mail representation of the notification.
     *
     * @param  mixed  $notifiable
     * @return \Illuminate\Notifications\Messages\MailMessage
     */
    public function toMail($notifiable)
    {
        return (new MailMessage)
                    ->success()
                    ->subject('School of Data | Confirmation d\'email')
                    ->line("Bonjour $notifiable->name")
                    ->line('Nous sommes prêts à activer votre compte. Il nous suffit de vérifier que cette adresse e-mail est bien la vôtre. ')
                    ->action('Confirmer mon compte', url("/confirm/{$notifiable->id}/". urlencode($notifiable->confirmation_token)))
                    ->line('Si vous n\'avez pas créé de compte Schoda, il vous suffit de supprimer cet e-mail et tout sera comme avant.')
                    ->line('Merci d\'utiliser notre application!');
    }

    /**
     * Get the array representation of the notification.
     *
     * @param  mixed  $notifiable
     * @return array
     */
    public function toArray($notifiable)
    {
        return [
            //
        ];
    }
}
